def plot_critical_profile(config: ValidationConfig, output_dir: Path) -> None:
    plt = require_matplotlib()
    tables = {
        property_name: load_property_table(config.table_dir, property_name)
        for property_name in PROPERTY_CONFIG
    }
    t_min, t_max, p_min, p_max = table_domain(tables)
    temperature_start = max(config.critical_t_min, t_min)
    temperature_stop = min(config.critical_t_max, t_max)
    pressure_pa = min(max(7.377298373446752e6, p_min), p_max)

    if temperature_stop <= temperature_start:
        return

    temperatures = [
        temperature_start + i * (temperature_stop - temperature_start) / 200
        for i in range(201)
    ]

    fig, axes = plt.subplots(2, 2, figsize=(10, 7), sharex=True)
    for ax, property_name in zip(axes.ravel(), PROPERTY_CONFIG.keys()):
        table = tables[property_name]
        key = PROPERTY_CONFIG[property_name]["coolprop_key"]
        cp_values: list[float] = []
        interp_values: list[float] = []

        for temperature in temperatures:
            try:
                cp_values.append(PropsSI(key, "T", temperature, "P", pressure_pa, config.fluid))
            except Exception:
                cp_values.append(math.nan)
            interp_values.append(bilinear_interpolate(table, pressure_pa, temperature))

        ax.plot(temperatures, cp_values, label="CoolProp", linewidth=1.5)
        ax.plot(temperatures, interp_values, label="CSV interpolation", linestyle="--", linewidth=1.2)
        ax.set_title(property_name)
        ax.grid(True, alpha=0.3)

    for ax in axes[-1]:
        ax.set_xlabel("Temperature / K")
    for ax in axes[:, 0]:
        ax.set_ylabel("Property value")
    axes[0, 0].legend()
    fig.suptitle(f"Critical-region profile at P = {pressure_pa / 1.0e6:.3f} MPa")
    fig.tight_layout()
    fig.savefig(output_dir / "critical_region_profile.png", dpi=300)
    plt.close(fig)


def write_report(config: ValidationConfig, summaries: list[dict[str, float | str]], output_dir: Path) -> None:
    lines = [
        "# CO2 2D Table Validation Report",
        "",
        f"- Table directory: `{config.table_dir}`",
        f"- Fluid: `{config.fluid}`",
        f"- Global random samples: `{config.samples}`",
        f"- Critical-region random samples: `{config.critical_samples}`",
        f"- Critical region: `{config.critical_t_min}-{config.critical_t_max} K`, "
        f"`{config.critical_p_min / 1.0e6}-{config.critical_p_max / 1.0e6} MPa`",
        "",
        "## Error Summary",
        "",
        "| Property | Region | Valid points | Mean rel. error | P95 rel. error | Max rel. error |",
        "|---|---:|---:|---:|---:|---:|",
    ]

    for row in summaries:
        lines.append(
            f"| {row['property']} | {row['region']} | {row['valid_points']} | "
            f"{float(row['mean_relative_error']):.6e} | "
            f"{float(row['p95_relative_error']):.6e} | "
            f"{float(row['max_relative_error']):.6e} |"
        )

    lines.extend(
        [
            "",
            "## Figures",
            "",
            "- `relative_error_boxplot.png`",
            "- `relative_error_vs_temperature.png`",
            "- `relative_error_vs_pressure.png`",
            "- `critical_region_profile.png`",
            "",
            "Relative errors are dimensionless in the CSV summaries. Figure axes show relative error in percent.",
        ]
    )

    (output_dir / "validation_report.md").write_text("\n".join(lines), encoding="utf-8")


def run_validation(config: ValidationConfig) -> None:
    config.output_dir.mkdir(parents=True, exist_ok=True)
    rows, summaries = validate_tables(config)
    write_dict_csv(config.output_dir / "validation_point_errors.csv", rows)
    write_dict_csv(config.output_dir / "validation_error_summary.csv", summaries)
    write_report(config, summaries, config.output_dir)

    if rows:
        plot_error_boxplot(config.output_dir, rows)
        plot_error_vs_temperature(config.output_dir, rows)
        plot_error_vs_pressure(config.output_dir, rows)
        plot_critical_profile(config, config.output_dir)

    print(f"Validation outputs saved to {config.output_dir}")


def positive_int(value: str) -> int:
    parsed = int(value)
    if parsed <= 0:
        raise argparse.ArgumentTypeError("value must be a positive integer")
    return parsed


def parse_args() -> ValidationConfig:
    parser = argparse.ArgumentParser(
        description="Validate CO2 2D CSV property tables against direct CoolProp calls."
    )
    parser.add_argument("--table-dir", type=Path, default=DEFAULT_TABLE_DIR, help="Directory containing CO2 CSV tables")
    parser.add_argument("--output-dir", type=Path, default=DEFAULT_OUTPUT_DIR, help="Directory for validation outputs")
    parser.add_argument("--fluid", default=DEFAULT_FLUID, help=f"CoolProp fluid name. Default: {DEFAULT_FLUID}")
    parser.add_argument("--samples", type=positive_int, default=DEFAULT_SAMPLES, help="Global random validation points")
    parser.add_argument("--seed", type=int, default=DEFAULT_SEED, help="Random seed")
    parser.add_argument(
        "--critical-samples",
        type=int,
        default=DEFAULT_CRITICAL_SAMPLES,
        help="Random validation points in the critical region; use 0 to disable",
    )
    parser.add_argument("--critical-t-min", type=float, default=DEFAULT_CRITICAL_T_MIN, help="Critical minimum T, K")
    parser.add_argument("--critical-t-max", type=float, default=DEFAULT_CRITICAL_T_MAX, help="Critical maximum T, K")
    parser.add_argument("--critical-p-min", type=float, default=DEFAULT_CRITICAL_P_MIN, help="Critical minimum P, Pa")
    parser.add_argument("--critical-p-max", type=float, default=DEFAULT_CRITICAL_P_MAX, help="Critical maximum P, Pa")

    args = parser.parse_args()
    if args.critical_samples < 0:
        raise SystemExit("--critical-samples must be non-negative")

    return ValidationConfig(
        table_dir=args.table_dir,
        output_dir=args.output_dir,
        fluid=args.fluid,
        samples=args.samples,
        seed=args.seed,
        critical_samples=args.critical_samples,
        critical_t_min=args.critical_t_min,
        critical_t_max=args.critical_t_max,
        critical_p_min=args.critical_p_min,
        critical_p_max=args.critical_p_max,
    )


if __name__ == "__main__":
    run_validation(parse_args())
